# Exercício 1: Em um contexto de orientação a objetos, como podemos definir o que são mensagens e qual a sua importância?

# Mensagens são a forma de se comunicar com os objetos. Ao enviar uma mensagem para um objeto você estará utilizando um método implementado na classe.
# Os métodos por sua vez tem acesso direto a todos os atributos de uma classe, podendo assim realizar diversas ações de acordo com sua implementação.
